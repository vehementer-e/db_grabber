		JOIN #gen2 b ON a.external_id = b.number

	--Сборка дат мин. реструкт
	DROP TABLE

	IF EXISTS #rest_union
		SELECT *
		INTO #rest_union
		FROM (
			SELECT DISTINCT a.external_id AS number, convert(DATE, a.[Реструктуризирован дата], 104) AS period_start
			FROM [risk].[REG_REPORT_KP_FOR_CBR] a
			LEFT JOIN dbo.dm_restructurings AS b ON b.number = a.external_id
			WHERE b.number IS NULL -- ни разу не встречается в dm_restructurings
				AND a.[Реструктуризирован дата] IS NOT NULL -- замечен с реструктуризацией в [REG_REPORT_KP_FOR_CBR]
			
			UNION ALL
			
			SELECT number, period_start
			FROM [dwh2].[dbo].[dm_restructurings]
			) a
