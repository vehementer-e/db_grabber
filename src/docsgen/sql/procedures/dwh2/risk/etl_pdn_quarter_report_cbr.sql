	--Сборка  обесценения
	DROP TABLE

	IF EXISTS #id_30_06;
		SELECT -- Договоры из CMR с дпд >0 на дату
			a.external_id, a.d, dpd
		INTO #id_30_06
		FROM dbo.dm_CMRStatBalance a
		WHERE d = @cr_date
			AND dpd > 0;

	DROP TABLE

	IF EXISTS #stg_dpd;
		SELECT *, 'dpd' AS comment
		INTO #stg_dpd
		FROM (
			SELECT -- Из цмр максимальная дата договора с дпд=0
				b.external_id, dateadd(day, 1, max(a.d)) d, b.dpd
			FROM dbo.dm_CMRStatBalance a
			RIGHT JOIN #id_30_06 b ON a.external_id = b.external_id
			WHERE a.dpd = 0
				AND a.d < @cr_date
			GROUP BY b.external_id, b.dpd
			
			UNION ALL
			
			SELECT -- Из кп для цб, дата отчета - дпд
				a.external_id, DATEADD(day, - a.dpd_prov + 1, a.r_date) d, a.dpd_prov
			FROM [risk].[REG_REPORT_KP_FOR_CBR] a
			WHERE a.r_date = @cr_date
				AND dpd_prov > 0
			) a;

	DROP TABLE

	IF EXISTS #stg_rest;
		SELECT a.number AS external_id, min(a.period_start) date_restr, 'restruct' AS comment
		INTO #stg_rest
		FROM #rest_union AS a
		WHERE a.period_start BETWEEN '2020-12-31'
				AND @cr_date
		GROUP BY a.number;

	DROP TABLE

	IF EXISTS #stg_bnkr;
		SELECT a.external_id, min(a.r_date) min_bnkr, 'bnkr' AS comment
		INTO #stg_bnkr
		FROM [risk].[REG_REPORT_KP_FOR_CBR] a
		WHERE a.flag_bnkr = 1
			AND r_date BETWEEN '2020-12-31'
				AND @cr_date
		GROUP BY a.external_id;

