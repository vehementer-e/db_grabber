CREATE PROCEDURE [risk].[etl_pdn_quarter_report_cbr] @cr_date DATE
AS
BEGIN
	--Сборка ген2 для источника дохода, дохода и платежей
	DROP TABLE

	IF EXISTS #base_impairment;
	
	DROP TABLE

	IF EXISTS #gen2
		SELECT a.number, a.bki_exp_amount, a.credit_exp, isnull(a.avg_income, 0) avg_income, isnull(a.income_amount, 0) income_amount, isnull(a.rosstat_income, 0) rosstat_income, isnull(a.bki_income, 0) bki_income, isnull(a.application_income, 0) application_income, isnull(a.call_verified_income, 0) call_verified_income, isnull(a.reg_income, 0) reg_income, a.pdn
		INTO #gen2
		FROM risk.pdn_calculation_2gen a

	--Проверяем источник дохода 
	DROP TABLE

	IF EXISTS #base_income_source
		SELECT a.external_id, b.avg_income, b.pdn, b.credit_exp + b.bki_exp_amount AS sum_exp, CASE 
				WHEN b.avg_income = b.income_amount
					OR (
						abs(b.avg_income - b.income_amount) < abs(b.avg_income - b.rosstat_income)
						AND abs(b.avg_income - b.income_amount) < abs(b.avg_income - b.bki_income)
						AND abs(b.avg_income - b.income_amount) < abs(b.avg_income - b.application_income)
						AND abs(b.avg_income - b.income_amount) < abs(b.avg_income - b.call_verified_income)
						AND abs(b.avg_income - b.income_amount) < abs(b.avg_income - b.reg_income)
						)
					THEN '1'
				WHEN b.avg_income = b.rosstat_income
					OR (
						abs(b.avg_income - b.rosstat_income) < abs(b.avg_income - b.income_amount)
						AND abs(b.avg_income - b.rosstat_income) < abs(b.avg_income - b.bki_income)
						AND abs(b.avg_income - b.rosstat_income) < abs(b.avg_income - b.application_income)
						AND abs(b.avg_income - b.rosstat_income) < abs(b.avg_income - b.call_verified_income)
						AND abs(b.avg_income - b.rosstat_income) < abs(b.avg_income - b.reg_income)
						)
					THEN '2'
				WHEN b.avg_income = b.bki_income
					OR (
						abs(b.avg_income - b.bki_income) < abs(b.avg_income - b.income_amount)
						AND abs(b.avg_income - b.bki_income) < abs(b.avg_income - b.rosstat_income)
						AND abs(b.avg_income - b.bki_income) < abs(b.avg_income - b.application_income)
						AND abs(b.avg_income - b.bki_income) < abs(b.avg_income - b.call_verified_income)
						AND abs(b.avg_income - b.bki_income) < abs(b.avg_income - b.reg_income)
						)
					THEN '10'
				WHEN b.avg_income = b.application_income
					OR (
						abs(b.avg_income - b.application_income) < abs(b.avg_income - b.income_amount)
						AND abs(b.avg_income - b.application_income) < abs(b.avg_income - b.rosstat_income)
						AND abs(b.avg_income - b.application_income) < abs(b.avg_income - b.bki_income)
						AND abs(b.avg_income - b.application_income) < abs(b.avg_income - b.call_verified_income)
						AND abs(b.avg_income - b.application_income) < abs(b.avg_income - b.reg_income)
						)
					THEN '3'
				WHEN b.avg_income = b.call_verified_income
					OR (
						abs(b.avg_income - b.call_verified_income) < abs(b.avg_income - b.income_amount)
						AND abs(b.avg_income - b.call_verified_income) < abs(b.avg_income - b.rosstat_income)
						AND abs(b.avg_income - b.call_verified_income) < abs(b.avg_income - b.bki_income)
						AND abs(b.avg_income - b.call_verified_income) < abs(b.avg_income - b.application_income)
						AND abs(b.avg_income - b.call_verified_income) < abs(b.avg_income - b.reg_income)
						)
					THEN '4'
				WHEN b.avg_income = b.reg_income
					OR (
						abs(b.avg_income - b.reg_income) < abs(b.avg_income - b.income_amount)
						AND abs(b.avg_income - b.reg_income) < abs(b.avg_income - b.rosstat_income)
						AND abs(b.avg_income - b.reg_income) < abs(b.avg_income - b.bki_income)
						AND abs(b.avg_income - b.reg_income) < abs(b.avg_income - b.application_income)
						AND abs(b.avg_income - b.reg_income) < abs(b.avg_income - b.call_verified_income)
						)
					THEN '2' --'reg_income'
				END AS source
		INTO #base_income_source
