	AS (
		SELECT *
		FROM #stg_bnkr
		
		UNION ALL
		
		SELECT external_id, d, comment
		FROM #stg_dpd
		
		UNION ALL
		
		SELECT *
		FROM #stg_rest
		)
	SELECT q1.external_id, min(q1.min_bnkr) AS dt_risk
	INTO #base_impairment
	FROM q1
	GROUP BY q1.external_id

