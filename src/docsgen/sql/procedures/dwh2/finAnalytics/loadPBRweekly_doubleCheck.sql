    a.[Снижение ставки (реструктуризация)], a.[Дата ДС9], a.[Кредитные каникулы по 377-ФЗ], a.[Дата ДС10], a.[Отмена Кредитных каникул по 377-ФЗ], a.[Дата ДС11], 
    a.[Пролонгация PDL], a.[Дата ДС12], a.[Отчетная дата], a.[ПСК для РВПЗ], a.created
        ,'WEEKLY'
    from stg.files.PBR_WEEKLY_stg a
    inner join (
        select
        l1.dogNum
        from (
        select
        [dogNum] = a.[Номер договора]
        ,[row_num] = row_number() over (Partition by a.[Номер договора] order by a.[№ п/п])
        from stg.files.PBR_WEEKLY_stg a--stg.files.PBR_weekly_stg a--stg.files.PBR_MONTHLY_stg a
        ) l1
        where l1.row_num>1
     ) b on a.[Номер договора]=b.dogNum
     
     DECLARE @procEndTimeraw datetime = getdate()   -- Переменная фиксирует окончание выполнения

     DECLARE @procStartTime varchar(40) = format(@procStartTimeraw,'dd.MM.yyyy HH:mm:ss', 'ru-RU')
     DECLARE @procEndTime varchar(40) =  format(@procEndTimeraw,'dd.MM.yyyy HH:mm:ss', 'ru-RU')
     DECLARE @timeDuration varchar(40) = concat(cast (datediff(second,@procStartTimeraw,@procEndTimeraw) as varchar),' секунд')

     declare @badDogNumsCount int = (select count(distinct [Номер договора]) from finAnalytics.PBR_load_err where convert(date,[Отчетная дата],104) = @repmonth and PBRLoadType='WEEKLY')
     declare @badDogNums nvarchar(max) = (select string_agg(l1.dogNum,', ') from (select distinct dogNum = [Номер договора] from finAnalytics.PBR_load_err where convert(date,[Отчетная дата],104) = @repmonth and PBRLoadType='WEEKLY') l1)

     if @badDogNumsCount>0 
     begin
     DECLARE @msg_find NVARCHAR(2048) = CONCAT (
				'Найдены задвоенные договора в недельном ПБР за '
                ,FORMAT( @REPMONTH, 'MMMM yyyy', 'ru-RU' )
                ,char(10)
                ,char(13)
                ,'Время начала выполнения: '
                ,@procStartTime
                ,char(10)
                ,char(13)
                ,'Время окончания выполнения: '
                ,@procEndTime
                ,char(10)
                ,char(13)
                ,'Время выполнения: '
                ,@timeDuration
                ,char(10)
                ,char(13)
                ,char(10)
                ,char(13)
                ,'Кол-во договоров: '
                ,@badDogNumsCount
                ,char(10)
                ,char(13)
                ,'Список договоров: '
                ,@badDogNums
                ,char(10)
                ,char(13)
                ,CONCAT ('Подробнее тут: ',(select link from finAnalytics.SYS_SPR_linkReport where repName='Дубликаты ПБР'))
                ,char(10)
                ,char(13)
                ,'В итоговую таблицу загружен только один из дубликатов с наименьшим порядковым номером.'
				)

	declare @emailList varchar(255)=''
	--настройка адресатов рассылки
	set @emailList = (select STRING_AGG(email,';') from finAnalytics.emailList where emailUID in (1,2,31,4,6))
    EXEC msdb.dbo.sp_send_dbmail @profile_name = 'Default'
			,@recipients = @emailList
			,@copy_recipients = ''
			,@body = @msg_find
			,@body_format = 'TEXT'
			,@subject = @subject;
    end

END
