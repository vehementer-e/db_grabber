    [Дата первой просрочки], [Задолженность ОД], [Задолженность проценты], [Сумма пени счета], [Сумма госпошлин счета], [Сумма просрочки основной долг],
    [Сумма просрочки проценты], [Дней просрочки], [Историческая просрочка], [Итого дней просрочки общая], [Максимальный срок просрочки по одному заемщику],
    [Процент резерва ОД], [Резерв ОД], [Процент резерва проценты], [Резерв проценты], Состояние, [Дней просрочки1], [Процент резерва ОД1], 
    [Процент резерва проценты1], [Сумма резерв прочие НУ], [Сумма резерва БУ ОД], [Сумма резерва БУ проценты], [Сумма резерва БУ пени и госпошлины], 
    [Вид ДС], [Ставка на дату выдачи], [Ставка на дату формирования отчета], ПСК, [ПСК первоначальная], [Обеспеченный займ], [ПДН на отчетную дату], 
    [Наличие просрочки], [Счет учета основного долга], [Счет учета начисления процентов], [Счет учета обеспечения], [Подразделение организации],
    [Адрес подразделения], [Контрагент код], ИНН, [Дней просрочки за последний год], [Дата закрыт], [Номенклатурная группа], [Акция 0%], [Дата последнего платежа по ОД],
    [Сумма последнего платежа по ОД], Сумма47422, [Счет аналитического учета47422], Забаланс, [Последний платеж 47422], [Сумма последнего платежа 47422],
    [Лицевой счет учета пени], [Дата последней проводки дт], [Дата последней проводки кт], [Дата погашения требований], [Способ закрытия обязательств],
    Кредитор, [Договор с кредитором], Маркирован, [Дата входа в залог], [Дата выхода из залога], [Заморозка (для резервов 30#06#21)], [Дата ДС], 
    [Заморозка 1#0], [Дата ДС1], [Изм#ставки, пополнение ОД], [Дата ДС2], [Кредитные каникулы], [Дата ДС3], [Отмена Реструктуризации], [Дата ДС4],
    Пролонгация, [Дата ДС5], Реструктуризация, [Дата ДС6], [Реструктуризация (перенос даты)], [Дата ДС7], Рефинансирование1, [Дата ДС8], 
    [Снижение ставки (реструктуризация)], [Дата ДС9], [Кредитные каникулы по 377-ФЗ], [Дата ДС10], [Отмена Кредитных каникул по 377-ФЗ], [Дата ДС11], 
    [Пролонгация PDL], [Дата ДС12], [Отчетная дата], PSK_PRC, created,  PBRLoadType)
    select
        a.[№ п/п], a.Контрагент, a.[Признак заемщика], a.Банкротство, a.[Досье контрагента (банкрот)], a.МСП, a.[Паспортные данные], a.[Дата рождения], 
    a.[Адрес для расчет ПДН], a.[Адрес проживания], a.[Адрес регистрации], a.[Финансовый продукт], a.[Номер договора], a.[Дата договора], a.[Дата выдачи], 
    a.[Способ выдачи займа], a.[Цессия (проданные займы)], a.[Дата погашения], a.[Дата погашения с учетом ДС], a.[Дата окончания по КК], a.[Срок договора в месяцах], 
    a.[Срок договора в днях], a.[Сумма займа], a.[Наличие залога поручительства], a.[Номер договора залога], a.[Дата договора залога], a.[Транспортное средство vin],
    a.[Vin в договоре], a.Докредитование, a.[Залоговая стоимость], a.[Залоговая стоимость распределение], a.[Справедливая стоимость], a.[Справедливая стоимость распределенная по vin], 
    a.[Справедливая стоимость проверка обеспеченности], a.[ПДН на дату выдачи (НЕ для ЦБ)], a.[Среднемесячный доход], a.[Первичная сумма], a.Рефинансирование, 
    a.Реструктуризирован, a.[Реструктуризирован номер], a.[Реструктуризирован дата], a.[Реструктуризирован дата окончания], a.[Реструктуризирован срок], 
    a.[Дата первой просрочки], a.[Задолженность ОД], a.[Задолженность проценты], a.[Сумма пени счета], a.[Сумма госпошлин счета], a.[Сумма просрочки основной долг],
    a.[Сумма просрочки проценты], a.[Дней просрочки], a.[Историческая просрочка], a.[Итого дней просрочки общая], a.[Максимальный срок просрочки по одному заемщику],
    a.[Процент резерва ОД], a.[Резерв ОД], a.[Процент резерва проценты], a.[Резерв проценты], a.Состояние, a.[Дней просрочки1], a.[Процент резерва ОД1], 
    a.[Процент резерва проценты1], a.[Сумма резерв прочие НУ], a.[Сумма резерва БУ ОД], a.[Сумма резерва БУ проценты], a.[Сумма резерва БУ пени и госпошлины], 
    a.[Вид ДС], a.[Ставка на дату выдачи], a.[Ставка на дату формирования отчета], a.ПСК, a.[ПСК первоначальная], a.[Обеспеченный займ], a.[ПДН на отчетную дату], 
    a.[Наличие просрочки], a.[Счет учета основного долга], a.[Счет учета начисления процентов], a.[Счет учета обеспечения], a.[Подразделение организации],
    a.[Адрес подразделения], a.[Контрагент код], a.ИНН, a.[Дней просрочки за последний год], a.[Дата закрыт], a.[Номенклатурная группа], a.[Акция 0%], a.[Дата последнего платежа по ОД],
    a.[Сумма последнего платежа по ОД], a.Сумма47422, a.[Счет аналитического учета47422], a.Забаланс, a.[Последний платеж 47422], a.[Сумма последнего платежа 47422],
    a.[Лицевой счет учета пени], a.[Дата последней проводки дт], a.[Дата последней проводки кт], a.[Дата погашения требований], a.[Способ закрытия обязательств],
