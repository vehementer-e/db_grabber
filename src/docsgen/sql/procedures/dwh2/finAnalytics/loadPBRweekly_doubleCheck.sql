
CREATE   PROCEDURE [finAnalytics].[loadPBRweekly_doubleCheck]
        @repmonth date
AS
BEGIN
    DECLARE @sp_name NVARCHAR(255) = OBJECT_NAME(@@PROCID)
    DECLARE @procStartTimeraw datetime = getdate()  -- Переменная фиксирует начало выполнения
    DECLARE @subject NVARCHAR(255) = 'Выполнение процедуры Проверки ПБР на дубли'
                /*
                CONCAT (
				'Выполнение процедуры Проверки ПБР на дубли'
				,@sp_name
				)
                */
    
    delete from finAnalytics.PBR_load_err where convert(date,[Отчетная дата],104) = @repmonth and PBRLoadType='WEEKLY'

    insert into finAnalytics.PBR_load_err
    ([№ п/п], Контрагент, [Признак заемщика], Банкротство, [Досье контрагента (банкрот)], МСП, [Паспортные данные], [Дата рождения], 
    [Адрес для расчет ПДН], [Адрес проживания], [Адрес регистрации], [Финансовый продукт], [Номер договора], [Дата договора], [Дата выдачи], 
    [Способ выдачи займа], [Цессия (проданные займы)], [Дата погашения], [Дата погашения с учетом ДС], [Дата окончания по КК], [Срок договора в месяцах], 
    [Срок договора в днях], [Сумма займа], [Наличие залога поручительства], [Номер договора залога], [Дата договора залога], [Транспортное средство vin],
