	ВТ_ЗадачиДляПричинОтказа.Дата,
	ПричинаОтказа = Справочник_CRM_ПричиныОтказов.Наименование
into #ВТ_ЗадачиСПричинойОтказа
from
	(
	select 
	ВТ_ЗадачиДляПричинОтказа.БизнесПроцесс_Ссылка,
	max(ВТ_ЗадачиДляПричинОтказа.Дата) as Дата
		from #ВТ_ЗадачиДляПричинОтказа as ВТ_ЗадачиДляПричинОтказа
	group by  ВТ_ЗадачиДляПричинОтказа.БизнесПроцесс_Ссылка
	)ВТ_МаксимальнаяЗадача 
		INNER JOIN #ВТ_ЗадачиДляПричинОтказа as ВТ_ЗадачиДляПричинОтказа
		ON ВТ_МаксимальнаяЗадача.БизнесПроцесс_Ссылка = ВТ_ЗадачиДляПричинОтказа.БизнесПроцесс_Ссылка
			AND ВТ_МаксимальнаяЗадача.Дата = ВТ_ЗадачиДляПричинОтказа.Дата
	left join stg._1cCRM.Документ_ЗаявкаНаЗаймПодПТС Документ_ЗаявкаНаЗаймПодПТС
		 on Документ_ЗаявкаНаЗаймПодПТС.Ссылка = ВТ_ЗадачиДляПричинОтказа.Предмет_Ссылка
			and ВТ_ЗадачиДляПричинОтказа.Предмет_ТипСсылки = 0x000022C4--Документ.ЗаявкаНаЗаймПодПТС
	left join stg._1cCRM.Документ_Обзвон Документ_Обзвон
		on Документ_Обзвон.Ссылка = ВТ_ЗадачиДляПричинОтказа.Предмет_Ссылка
			and ВТ_ЗадачиДляПричинОтказа.Предмет_ТипСсылки = 0x00002486
	left join stg._1cCRM.Документ_CRM_Заявка Документ_CRM_Заявка
		on Документ_CRM_Заявка.Ссылка = ВТ_ЗадачиДляПричинОтказа.Предмет_Ссылка
			and ВТ_ЗадачиДляПричинОтказа.Предмет_ТипСсылки = 0x000021F5
	left join stg._1cCRM.Документ_CRM_Интерес Документ_CRM_Интерес  
		on Документ_CRM_Интерес.Ссылка =ВТ_ЗадачиДляПричинОтказа.Предмет_Ссылка
			and ВТ_ЗадачиДляПричинОтказа.Предмет_ТипСсылки = 0x000000BC
			and Документ_CRM_Интерес.[ПричинаОтказа_ТипСсылки] = 0x00000038
	left join stg.[_1cCRM].[Справочник_CRM_ПричиныОтказов] Справочник_CRM_ПричиныОтказов
		on Справочник_CRM_ПричиныОтказов.Ссылка = COALESCE(
			NULLIF(Документ_ЗаявкаНаЗаймПодПТС.ПричинаОтказа, 0x)
			,NULLIF(Документ_Обзвон.ПричинаОтказа, 0x)
			,nullif(Документ_CRM_Интерес.ПричинаОтказа_Ссылка, 0x)
			,NULLIF(Документ_CRM_Заявка.ПричинаОтказа, 0x)
			,0x)
;

