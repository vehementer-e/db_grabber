-- Description:	DWH-1489. 1. Добавить столбец Номер заявки, в нем хранить 14-значный номер заявки
--	  2. Хранить номер телефона без спец. символов, без 7 и 8
--[dbo].[create_dm_Все_коммуникации_На_основе_отчета_из_crm] @reloadReportDate = '2025-09-01'
-- =======================================================
CREATE PROC dbo.create_dm_Все_коммуникации_На_основе_отчета_из_crm
	@reloadReportDate date  = NULL,
	@phone_bin_id binary(16) = null, --ТелефонныйЗвонок.Ссылка
	@isDebug int = 0
as
begin
begin try
SELECT @isDebug = isnull(@isDebug, 0)
declare @dLastReportDate date= '2021-11-02'
	
	if OBJECT_ID('dbo.dm_Все_коммуникации_На_основе_отчета_из_crm') is not null
		and @phone_bin_id is null
	begin
		set @dLastReportDate = 
		dateadd(dd,-1, 
			COALESCE(@reloadReportDate, 
				(select max(ДатаВзаимодействия) from dbo.dm_Все_коммуникации_На_основе_отчета_из_crm)
				, @dLastReportDate)
			)
	end
	 


declare @ПланВидовХарактеристик_CRM_НастройкиПользователей  binary(16) = (
	select TOP(1) [Ссылка] from stg.[_1cCRM].[ПланВидовХарактеристик_CRM_НастройкиПользователей]
	where Наименование = 'Текущий внутренний номер')
	

drop table if exists #ВТ_ВнутренниеТелефоны
select 
	CRM_НастройкиПользователей.Пользователь,
	max(CRM_НастройкиПользователей.Значение_Строка) as НомерТелефонаОператора
into #ВТ_ВнутренниеТелефоны
from stg.[_1cCRM].РегистрСведений_CRM_НастройкиПользователей as CRM_НастройкиПользователей
where  CRM_НастройкиПользователей.Настройка = @ПланВидовХарактеристик_CRM_НастройкиПользователей
	and NULLIF(CRM_НастройкиПользователей.Значение_Строка,'') is not null
group by CRM_НастройкиПользователей.Пользователь

IF @isDebug = 1 BEGIN
	DROP TABLE IF EXISTS ##ВТ_ВнутренниеТелефоны
	SELECT * INTO ##ВТ_ВнутренниеТелефоны FROM #ВТ_ВнутренниеТелефоны
END


drop table if exists #ВТ_ВсеЗвонки
select distinct
	Звонок_Ссылка			= ТелефонныйЗвонок.Ссылка,
	ТелефонныйЗвонок.Тема,
	ВзаимодействиеОснование_Ссылка = nullif(ТелефонныйЗвонок.ВзаимодействиеОснование_Ссылка, 0x),
	Задача_Ссылка			= nullif(Документ_CRM_Взаимодействие.Задача, 0x) ,
	Session_id				= ТелефонныйЗвонок.Session_id,
	ЗадачаДата				= dateadd(year,-2000, Задача.Дата),
	Задача_Наименование		= iif(Задача.ССылка is not null, 
	Concat('Задача ', 
		' ',
		TRIM(Задача.Номер),
		', ',
		TRIM(Задача.Наименование),
		' от ',
		format(dateadd(year,-2000, Задача.Дата ), 'dd.MM.yyyy hh:mm:ss')
	), null),
	

	Задача.[CRM_ВариантВыполненияСтрокой],
	CRM_ТочкаМаршрута_Ссылка = Задача.CRM_ТочкаМаршрута,
	Справочник_CRM_ТочкиМаршрутов_Наименование = Справочник_CRM_ТочкиМаршрутов.Наименование,
	Справочник_CRM_ТочкиМаршрутов_ИспользоватьАвтоматическоеВыполнениеЗадач = Справочник_CRM_ТочкиМаршрутов.ИспользоватьАвтоматическоеВыполнениеЗадач,
	БизнесПроцесс_Ссылка	= nullif(Задача.[БизнесПроцесс_Ссылка], 0x),
	CRM_БизнесПроцесс.Предмет_Ссылка,
	CRM_БизнесПроцесс.Предмет_ТипСсылки,
	ЗвонокДата				= dateadd(year,-2000,ТелефонныйЗвонок.Дата),
	ЗвонокАвтор_Ссылка		= ТелефонныйЗвонок.[Автор],
	ЗвонокВходящий			= cast(ТелефонныйЗвонок.[Входящий]as bit),
	ТелефонныйЗвонок.АбонентКакСвязаться,
	ТелефонныйЗвонок.АбонентПредставление,
	ТелефонныйЗвонок.сфпДлительностьЗвонка
	

into #ВТ_ВсеЗвонки
from stg._1ccrm.Документ_ТелефонныйЗвонок as ТелефонныйЗвонок
	LEFT JOIN stg.[_1cCRM].Документ_CRM_Взаимодействие Документ_CRM_Взаимодействие
		ON ТелефонныйЗвонок.ВзаимодействиеОснование_Ссылка = Документ_CRM_Взаимодействие.Ссылка
		and ТелефонныйЗвонок.ВзаимодействиеОснование_ТипСсылки = 0x000000BB
	left join stg.[_1cCRM].[Задача_ЗадачаИсполнителя] Задача 
		on Задача.[Ссылка] = Документ_CRM_Взаимодействие.Задача
	left join stg.[_1cCRM].Справочник_CRM_ТочкиМаршрутов Справочник_CRM_ТочкиМаршрутов
		on Справочник_CRM_ТочкиМаршрутов.Ссылка = Задача.CRM_ТочкаМаршрута
	left join stg.[_1cCRM].[БизнесПроцесс_CRM_БизнесПроцесс] CRM_БизнесПроцесс 
		on CRM_БизнесПроцесс.[Ссылка] = Задача.[БизнесПроцесс_Ссылка]
			and Задача.[БизнесПроцесс_ТипСсылки] =0x00000010
where
	dateadd(year,-2000,ТелефонныйЗвонок.Дата) >= @dLastReportDate
	--dateadd(year,-2000,ТелефонныйЗвонок.Дата) between @ДатаНачала and  @ДатаОкончания
	and (ТелефонныйЗвонок.Ссылка = @phone_bin_id or @phone_bin_id is null)
;

IF @isDebug = 1 BEGIN
	DROP TABLE IF EXISTS ##ВТ_ВсеЗвонки
	SELECT * INTO ##ВТ_ВсеЗвонки FROM #ВТ_ВсеЗвонки
END



drop table if exists #ВТ_Взаимодействия
select
	Взаимодействие_Ссылка = CRM_ДокументыВзаимодействия.Взаимодействие,
	Звонок_Ссылка = CRM_ДокументыВзаимодействия.Документ_Ссылка
into #ВТ_Взаимодействия
from
	stg._1cCRM.РегистрСведений_CRM_ДокументыВзаимодействия 
		as CRM_ДокументыВзаимодействия
where exists(select top(1) 1 from 
	#ВТ_ВсеЗвонки вз
	where вз.Звонок_Ссылка = CRM_ДокументыВзаимодействия.Документ_Ссылка
		and CRM_ДокументыВзаимодействия.Документ_ТипСсылки = 0x000000CA
		and вз.ВзаимодействиеОснование_Ссылка = CRM_ДокументыВзаимодействия.Взаимодействие --для решения CRM-6505
		)
	

create clustered index ix on #ВТ_Взаимодействия (Звонок_Ссылка, Взаимодействие_Ссылка)

IF @isDebug = 1 BEGIN
	DROP TABLE IF EXISTS ##ВТ_Взаимодействия
	SELECT * INTO ##ВТ_Взаимодействия FROM #ВТ_Взаимодействия
END


drop table if exists #ВТ_ВсеВзаимодействия

select 
	Взаимодействие_Ссылка = CRM_Взаимодействие.Ссылка,
	Взаимодействие_Наименование =Concat(
		'Взаимодействие',
		' ' ,
		CRM_Взаимодействие.Номер,
		' от ',
		format(dateadd(year, -2000, CRM_Взаимодействие.Дата),  'dd.MM.yyyy hh:mm:ss')
		),

	Задача_Ссылка		= nullif(CRM_Взаимодействие.Задача, 0x),
	ПотенциальныйКлиент_Ссылка = nullif(CRM_Взаимодействие.ПотенциальныйКлиент,0x),
	Партнер_Ссылка		= nullif(CRM_Взаимодействие.Партнер, 0x) ,
	КонтактноеЛицо_Ссылка	= nullif(CRM_Взаимодействие.КонтактноеЛицо, 0x),
	ВидВзаимодействия_Ссылка = nullif(CRM_Взаимодействие.ВидВзаимодействия, 0x),
	ВидыВзаимодействий_Направление_Ссылка = nullif(CRM_ВидыВзаимодействий.Направление, 0x),
	ВидыВзаимодействий_Наименование = CRM_ВидыВзаимодействий.Наименование,

	Автор_Ссылка		= nullif(CRM_Взаимодействие.Автор, 0x),
	Заявка_Ссылка		= nullif(CRM_Взаимодействие.Заявка_Ссылка, 0x),
	Заявка_ТипСсылки	= nullif(CRM_Взаимодействие.Заявка_ТипСсылки, 0x),
	ДокументОснование_Ссылка	= nullif(CRM_Взаимодействие.ДокументОснование_Ссылка, 0x),
	ДокументОснование_ТипСсылки = nullif(CRM_Взаимодействие.ДокументОснование_ТипСсылки, 0x),
	БизнесПроцесс_Ссылка		= nullif(Задача.[БизнесПроцесс_Ссылка], 0x),
	Предмет_Ссылка 		= nullif(CRM_БизнесПроцесс.Предмет_Ссылка, 0x),
	Предмет_ТипСсылки	= nullif(CRM_БизнесПроцесс.Предмет_ТипСсылки, 0x),
	--Интерес_Ссылка		= nullif(CRM_БизнесПроцесс.Интерес_Ссылка, 0x),
	--Интерес_ТипСсылки	= nullif(CRM_БизнесПроцесс.Интерес_ТипСсылки, 0x),
	ТемыОбращений_Наименование = ТемыОбращений.Наименование,
	ДеталиОбращения_Ссылка = nullif(CRM_Взаимодействие.ДеталиОбращения, 0x),
	ДеталиОбращения_Наименование = ДеталиОбращений.[Наименование],
	ТипУслугиПоУмолчанию = CRM_ТипУслуги.[Наименование],
	CRM_Взаимодействие.Результат,
	CRM_Взаимодействие.Комментарий,
	ПоддеталиОбращения_Ссылка =nullif(CRM_Взаимодействие.[ПоддеталиОбращения], 0x),
	ПоддеталиОбращений_Наименование = ПоддеталиОбращений.Наименование,
	ДатаВзаимодействие	= dateadd(year,-2000, CRM_Взаимодействие.Дата),
	ЗавершившийПользователь_Ссылка = nullif(CRM_Взаимодействие.[ЗавершившийПользователь], 0x)
into #ВТ_ВсеВзаимодействия
from 
(
select *  from stg.[_1cCRM].Документ_CRM_Взаимодействие 
where dateadd(year,-2000,Дата) >= @dLastReportDate
	and @phone_bin_id is null
union
select * from stg.[_1cCRM].Документ_CRM_Взаимодействие 
where exists (select top(1) 1 from #ВТ_Взаимодействия  Взаимодействия 
	where Документ_CRM_Взаимодействие.Ссылка = Взаимодействия.Взаимодействие_Ссылка
	)
) as CRM_Взаимодействие
	left join stg.[_1cCRM].[Задача_ЗадачаИсполнителя] Задача 
		on Задача.[Ссылка] = CRM_Взаимодействие.Задача
	left join stg.[_1cCRM].[БизнесПроцесс_CRM_БизнесПроцесс] CRM_БизнесПроцесс 
		on CRM_БизнесПроцесс.[Ссылка] = Задача.[БизнесПроцесс_Ссылка]
			and Задача.[БизнесПроцесс_ТипСсылки] =0x00000010
	left join stg.[_1cCRM].[Справочник_ТемыОбращений] ТемыОбращений 
		on ТемыОбращений.Ссылка = CRM_Взаимодействие.ТемаСсылка
	left join stg.[_1cCRM].[Справочник_CRM_ТипУслуги] CRM_ТипУслуги 
		ON CRM_ТипУслуги.[Ссылка] = ТемыОбращений.ТипУслугиПоУмолчанию	
	left join stg._1cCRM.Справочник_CRM_ВидыВзаимодействий CRM_ВидыВзаимодействий
		ON CRM_ВидыВзаимодействий.Ссылка = CRM_Взаимодействие.ВидВзаимодействия
	LEFT JOIN stg._1cCRM.Справочник_ДеталиОбращений ДеталиОбращений 
		ON ДеталиОбращений.Ссылка =  CRM_Взаимодействие.ДеталиОбращения
	LEFT JOIN stg.[_1cCRM].[Справочник_ПоддеталиОбращений] ПоддеталиОбращений
		ON ПоддеталиОбращений.Ссылка = CRM_Взаимодействие.[ПоддеталиОбращения]

IF @isDebug = 1 BEGIN
	DROP TABLE IF EXISTS ##ВТ_ВсеВзаимодействия
	SELECT * INTO ##ВТ_ВсеВзаимодействия FROM #ВТ_ВсеВзаимодействия
END


drop table if exists #ВТ_ВзаимодействияБезЗвонка
select 
	ВТ_ВсеВзаимодействия.Взаимодействие_Ссылка,
	Взаимодействие_Наименование,
	ВидыВзаимодействий_Направление_Ссылка,
	Автор_Ссылка,
	Автор_ФИО = Справочник_Пользователи_Автор.Наименование,
	ЗавершившийПользователь_Ссылка,
	ЗавершившийПользователь_ФИО = Справочник_Пользователи_ЗавершившийПользователь.Наименование,
	Заявка_Ссылка,
	Заявка_ТипСсылки,
	ДокументОснование_Ссылка,
	ДокументОснование_ТипСсылки,
	Задача_Ссылка	= Задача.Ссылка,
	Задача_Наименование =Задача.Наименование,
	Задача_Дата		= dateadd(year, -2000, Задача.Дата),
	Задача_CRM_ТочкаМаршрута = Задача.CRM_ТочкаМаршрута,

	Справочник_CRM_ТочкиМаршрутов_Наименование = Справочник_CRM_ТочкиМаршрутов.Наименование,
	
	Задача_CRM_ВариантВыполненияСтрокой = Задача.CRM_ВариантВыполненияСтрокой,
	ВТ_ВсеВзаимодействия.Предмет_Ссылка,
	ВТ_ВсеВзаимодействия.Предмет_ТипСсылки,
	ВТ_ВсеВзаимодействия.ПотенциальныйКлиент_Ссылка,
	ВТ_ВсеВзаимодействия.Партнер_Ссылка,
	ВТ_ВсеВзаимодействия.КонтактноеЛицо_Ссылка,
	ВТ_ВсеВзаимодействия.ДатаВзаимодействие,
	ВТ_ВсеВзаимодействия.ТемыОбращений_Наименование,
	ВТ_ВсеВзаимодействия.ДеталиОбращения_Ссылка,
	ВТ_ВсеВзаимодействия.ДеталиОбращения_Наименование,
	ВТ_ВсеВзаимодействия.ТипУслугиПоУмолчанию,
	ВТ_ВсеВзаимодействия.Результат,
	ВТ_ВсеВзаимодействия.Комментарий,
	ВТ_ВсеВзаимодействия.ПоддеталиОбращения_Ссылка,
	ВТ_ВсеВзаимодействия.ПоддеталиОбращений_Наименование,
	
	ЗаявкаНаЗаймПодПТС_Ссылка = CASE
		WHEN ВТ_ВсеВзаимодействия.Предмет_ТипСсылки  = 0x000022C4 --Документ.ЗаявкаНаЗаймПодПТС
			THEN ВТ_ВсеВзаимодействия.Предмет_Ссылка
		WHEN ВТ_ВсеВзаимодействия.Заявка_ТипСсылки = 0x000022C4 --Документ.ЗаявкаНаЗаймПодПТС
			THEN ВТ_ВсеВзаимодействия.Заявка_Ссылка
	END,
	CRM_Заявка_Ссылка = CASE 
		WHEN  ВТ_ВсеВзаимодействия.Предмет_ТипСсылки = 0x000021F5--Документ.CRM_Заявка
			THEN ВТ_ВсеВзаимодействия.Предмет_Ссылка
		WHEN ВТ_ВсеВзаимодействия.Заявка_ТипСсылки = 0x000021F5 --Документ.CRM_Заявка
			THEN ВТ_ВсеВзаимодействия.Заявка_Ссылка
	END, 
	CRM_Интерес_Ссылка = CASE 
		WHEN ВТ_ВсеВзаимодействия.Предмет_ТипСсылки  = 0x000000BC--Документ.CRM_Интерес
			THEN ВТ_ВсеВзаимодействия.Предмет_Ссылка
		WHEN ВТ_ВсеВзаимодействия.ДокументОснование_ТипСсылки  = 0x000000BC--Документ.CRM_Интерес
			THEN ВТ_ВсеВзаимодействия.ДокументОснование_Ссылка
	END,
	ВТ_ВсеВзаимодействия.ВидыВзаимодействий_Наименование
into #ВТ_ВзаимодействияБезЗвонка
from 
	#ВТ_ВсеВзаимодействия as ВТ_ВсеВзаимодействия
	LEFT JOIN stg._1cCRM.Справочник_Пользователи Справочник_Пользователи_Автор 
		ON Справочник_Пользователи_Автор.Ссылка = ВТ_ВсеВзаимодействия.Автор_Ссылка		
	LEFT JOIN stg._1cCRM.Справочник_Пользователи Справочник_Пользователи_ЗавершившийПользователь 
		ON Справочник_Пользователи_ЗавершившийПользователь.Ссылка = ВТ_ВсеВзаимодействия.ЗавершившийПользователь_Ссылка
	LEFT JOIN stg._1cCRM.Задача_ЗадачаИсполнителя Задача ON Задача.Ссылка = ВТ_ВсеВзаимодействия.ДокументОснование_Ссылка
		and ВТ_ВсеВзаимодействия.ДокументОснование_ТипСсылки = 0x00000014 
	LEFT JOIN stg._1cCRM.Справочник_CRM_ТочкиМаршрутов ON Справочник_CRM_ТочкиМаршрутов.Ссылка =Задача.CRM_ТочкаМаршрута

where not exists(
	select top(1) 1 from #ВТ_Взаимодействия ВТ_Взаимодействия_Звонок
	where ВТ_Взаимодействия_Звонок.Взаимодействие_Ссылка = ВТ_ВсеВзаимодействия.Взаимодействие_Ссылка
)
;

IF @isDebug = 1 BEGIN
	DROP TABLE IF EXISTS ##ВТ_ВзаимодействияБезЗвонка
	SELECT * INTO ##ВТ_ВзаимодействияБезЗвонка FROM #ВТ_ВзаимодействияБезЗвонка
END


declare @ТипыКонтактнойИнформации_Телефон varbinary(16)=(
	select top(1) Ссылка from stg.[_1cCRM].[Перечисление_ТипыКонтактнойИнформации]
	where Имя = 'Телефон'
	)
	
drop table if exists #ВТ_ТелефонИзИнтереса
select 
	ВТ_ВзаимодействияБезЗвонка.CRM_Интерес_Ссылка as Интерес_Ссылка,
	
	НомерТелефона = max(case
			when ВТ_ВзаимодействияБезЗвонка.ПотенциальныйКлиент_Ссылка is not null
				then CRM_ПотенциальныеКлиентыКонтактнаяИнформация.НомерТелефона
			when ВТ_ВзаимодействияБезЗвонка.Партнер_Ссылка is not null
				then ПартнерыКонтактнаяИнформация.НомерТелефона
			when ВТ_ВзаимодействияБезЗвонка.КонтактноеЛицо_Ссылка is not null
				then КонтактныеЛицаПартнеровКонтактнаяИнформация.НомерТелефона
			else CRM_ПотенциальныеКлиентыКонтактнаяИнформация.НомерТелефона
		end ) 
into #ВТ_ТелефонИзИнтереса
from
	#ВТ_ВзаимодействияБезЗвонка as ВТ_ВзаимодействияБезЗвонка
		LEFT JOIN stg._1cCRM.Справочник_CRM_ПотенциальныеКлиенты_КонтактнаяИнформация as CRM_ПотенциальныеКлиентыКонтактнаяИнформация
