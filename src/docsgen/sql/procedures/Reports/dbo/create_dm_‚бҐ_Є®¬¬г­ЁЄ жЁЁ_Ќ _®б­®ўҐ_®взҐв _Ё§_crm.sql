		ON Документ_Обзвон_Предмет.Ссылка =ВТ_ВзаимодействияБезЗвонка.Предмет_Ссылка 
			and ВТ_ВзаимодействияБезЗвонка.Предмет_ТипСсылки = 0x00002486 --Документ.Обзвон
	--DWH-2461
	LEFT JOIN stg._1cCRM.Документ_ЗаявкаНаЗаймПодПТС AS ЗаявкаНаЗаймПодПТС_2
		ON ЗаявкаНаЗаймПодПТС_2.Ссылка = Документ_Обзвон_Предмет.ЗаявкаОснование
