					, Count(m1.[ЗаявкаСсылка]) over(partition by m1.[ЗаявкаСсылка]) ВсегоВстречСчитаяОднуНаДень
					, row_number() over(partition by m1.[ЗаявкаСсылка]  order by m1.[ПоследняяДатаВстречиНаДню]  asc)  НомерВстречиПоДнямДляАнализаПереноса
