				, a3.ПоследняяДатаВстречиНаДню as ДатаВстречи3
				, a3.Сумма Сумма3 
				
				FROM #ttt a
				left join (select * from #ttt where РазницаДнейМеждуДатойФильтра_и_ДатойВстречи=0) a0 on a.ЗаявкаСсылка=a0.ЗаявкаСсылка
