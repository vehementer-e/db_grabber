				, iif(cast(a.[ПоследняяДатаВстречиНаДню] as date) = cast(a.ДатаСтатусаНеПустая as date),1,0) as Состоялась
				, a.[ПоследняяДатаВстречиНаДню] Дата

				, a.Сумма сумма	
				, a.НомерВстречиПоДнямДляАнализаПереноса
				
				, iif(cast(a0.[ПоследняяДатаВстречиНаДню] as date) = cast(a0.ДатаСтатусаНеПустая as date),1,0) as Состоялась0
				, a0.[ПоследняяДатаВстречиНаДню] as ДатаВстречи
				, a0.Сумма Сумма0

				, iif(cast(a1.[ПоследняяДатаВстречиНаДню] as date) = cast(a1.ДатаСтатусаНеПустая as date),1,0) as Состоялась1
				, a1.ПоследняяДатаВстречиНаДню as ДатаВстречи1
