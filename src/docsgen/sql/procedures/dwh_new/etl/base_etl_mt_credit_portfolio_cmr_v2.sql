,[ОстЗадолжГоспошлина] decimal(15,2) null
,[ПереплатаНачислено] decimal(15,2) null 
,[ПереплатаНачислено_Накоп] decimal(15,2) null 
,[ПереплатаОплачено] decimal(15,2) null 
,[ПереплатаОплачено_Накоп]  decimal(15,2) null
