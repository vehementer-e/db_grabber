--)
,	cmr_laststatus as
(
select d.[Ссылка] as [Договор] ,st.[Статус] as [ТекСатус] 
	   ,null as [Активен_cmr] --cs.[Активен] as [Активен_cmr] 
	   ,null as [Обслуживается_cmr]--cs.[Обслуживается] as [Обслуживается_cmr] 
from [Stg].[_1cCMR].[Справочник_Договоры] d  with (nolock)
--left join cmr_serv cs
--on d.[Ссылка]=cs.[Договор]
left join (select [Договор] ,[Статус]
		   from (select [Период] ,[Договор] ,[Статус] 
						,rank() over(partition by [Договор] order by [Период] desc) as [rank_p] 
				 from [Stg].[_1cCMR].[РегистрСведений_СтатусыДоговоров] with (nolock)
				 ) ls
			where ls.[rank_p]=1
		   ) st
on d.[Ссылка]=st.[Договор]
)
,	cmr_res as
(
select r.[Ссылка] ,r.[Код] as [ДоговорНомер] ,r.[Сумма]
	  ,r.[ДатаУчета] ,r.[Договор] --,r.[ДоговорНомер]
      ,isnull(r.[ДоговорСумма] ,r.[Сумма]) as [ДоговораСумма]

      ,r.[ОДНачислено] ,r.[ОДНачислено_Накоп] ,r.[ОДОплачено] ,r.[ОДОплачено_Накоп] 
	  ,(r.[Сумма] - isnull(r.[ОДОплачено_Накоп],0)) as [ОстЗадолжОД]

      ,r.[ПроцНачислено] ,r.[ПроцНачислено_Накоп] ,r.[ПроцОплачено] ,r.[ПроцОплачено_Накоп] 
	  ,(isnull(r.[ПроцНачислено_Накоп],0)-isnull(r.[ПроцОплачено_Накоп],0)) as [ОстЗадолжПроц]

      ,r.[ПениНачислено] ,r.[ПениНачислено_Накоп] ,r.[ПениОплачено] ,r.[ПениОплачено_Накоп] 
	  ,(isnull(r.[ПениНачислено_Накоп],0)-isnull(r.[ПениОплачено_Накоп],0)) as [ОстЗадолжПени]

      ,r.[ГоспошлинаНачислено] ,r.[ГоспошлинаНачислено_Накоп] ,[ГоспошлинаОплачено] ,r.[ГоспошлинаОплачено_Накоп] 
	  ,(isnull(r.[ГоспошлинаНачислено_Накоп],0)-isnull(r.[ГоспошлинаОплачено_Накоп],0)) as [ОстЗадолжГоспошлина]

      ,r.[ПереплатаНачислено] ,r.[ПереплатаНачислено_Накоп] ,r.[ПереплатаОплачено] ,r.[ПереплатаОплачено_Накоп] 
	  ,(isnull(r.[ПереплатаНачислено_Накоп],0)-isnull(r.[ПереплатаОплачено_Накоп],0)) as [ОстЗадолжПереплата]

      ,r.[ПроцГрейсНачислено] ,r.[ПроцГрейсНачислено_Накоп] ,r.[ПроцГрейсОплачено] ,r.[ПроцГрейсОплачено_Накоп] 
	  ,(isnull(r.[ПроцГрейсНачислено_Накоп],0)-isnull(r.[ПроцГрейсОплачено_Накоп],0)) as [ОстЗадолжПроцГрейс]

      ,r.[ПениГрейсНачислено] ,r.[ПениГрейсНачислено_Накоп] ,r.[ПениГрейсОплачено] ,r.[ПениГрейсОплачено_Накоп] 
	  ,(isnull(r.[ПениГрейсНачислено_Накоп],0)-isnull(r.[ПениГрейсОплачено_Накоп],0)) as [ОстЗадолжПениГрейс]

	  ,isnull(r2.[КоличествоПолныхДнейПросрочки],0) as [КолвоПолнДнПросрочки]

	  ,r2.[Бакет_cmr] 
    
	  ,r2.[ДатаВозникновенияПросрочки]

	  ,sd.[Наименование] as [ТекСатус]

	  ,rs.[СтатусПоДням]
	  ,r3.[Активен_cmr] ,r3.[Обслуживается_cmr]


from cmr_st r
left join cmr_st0 r2
on r.[Договор]=r2.[Ссылка] and r.[ДатаУчета]=r2.[ДатаУчета]
left join cmr_StatOnDay rs
on r.[Договор]=rs.[Ссылка] and r.[ДатаУчета]=rs.[ДатаУчета]
left join cmr_laststatus r3
on r.[Договор]=r3.[Договор]
left join cmr_StatusName sd
on r3.[ТекСатус]=sd.[Ссылка]
)

