create table [dwh_new].[dbo].[mt_credit_portfolio_cmr_v2]
(
[Ссылка] binary(16) null
,[ДоговорНомер] nvarchar(255)
,[Сумма] decimal(15,2) null
,[ДатаУчета] datetime
,[Договор] binary(16) null
--,r.[ДоговорНомер]
,[ДоговораСумма] decimal(15,2) null
,[ОДНачислено] decimal(15,2) null 
,[ОДНачислено_Накоп] decimal(15,2) null 
