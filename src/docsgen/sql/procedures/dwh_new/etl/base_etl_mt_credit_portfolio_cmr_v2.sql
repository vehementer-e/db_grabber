,[ПроцГрейсОплачено] decimal(15,2) null 
,[ПроцГрейсОплачено_Накоп] decimal(15,2) null 
,[ОстЗадолжПроцГрейс] decimal(15,2) null
,[ПениГрейсНачислено]  decimal(15,2) null
,[ПениГрейсНачислено_Накоп] decimal(15,2) null 
,[ПениГрейсОплачено] decimal(15,2) null
